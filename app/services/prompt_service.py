    def get_keywords_filter_prompt(
            self,
            category: str,
            purposes: List[str],
            additional_params: List[str],
            category_description: str = "",
            max_keywords: int = 25
    ) -> tuple[str, str]:
        """
        Промпт для фильтрации ключевых слов через GPT

        Returns:
            Кортеж (system_prompt, user_prompt)
        """
        purposes_text = ", ".join(purposes) if isinstance(purposes, list) else str(purposes)
        params_text = ", ".join(additional_params) if additional_params else "не указаны"

        system_prompt = f"""Ты — профессиональный маркетолог-копирайтер для маркетплейсов Wildberries и OZON. 
        Твоя задача — отобрать ТОЛЬКО {max_keywords} самых продающих и релевантных ключевых слов из списка 
        для создания заголовков и описаний товаров."""

        user_prompt = f"""
        Контекст товара:
        - Категория: {category}
        - Назначения: {purposes_text}
        - Доп. параметры: {params_text}
        - Описание категории: {category_description if category_description else "не указано"}

        Критерии отбора ключевых слов (В ПРИОРИТЕТЕ):
        1. **Конкретика** — точные названия товаров/свойств
        2. **Пользовательский язык** — как ищут реальные покупатели
        3. **Релевантность категории** — точно соответствуют "{category}"
        4. **Учет назначений** — подходят для "{purposes_text}"
        5. **SEO-оптимизация** — популярные поисковые запросы на маркетплейсах
        6. **Коммерческий потенциал** — слова, которые конвертируют в продажи

        ИСКЛЮЧАЙ:
        - Общие слова без конкретики (типа "товар", "изделие")
        - Технические термины, не понятные покупателям
        - Слова с ошибками или опечатками
        - Слишком длинные фразы (больше 3 слов)
        - Устаревшие или непопулярные запросы
        
        ТВОЯ ЗАДАЧА:
        - найти такие слова который охарактеризовывали бы товар, например, у нас есть 
        "Панель ПВХ стеновая под мрамор белая влагостойкая 2700×250 мм", здесь ключевыми словами будет: стеновая, белая, влагостойкая. Это хороший пример включения в заголовок ключевых слов.
        есть такой пример: "Декоративные деревянные панели для интерьера под дерево звукоизоляция защита", здесь ключевые слова звукоизоляция, защита, это, очевидно, не самые важные слова для покупателя. Твоя задача сопоставить входные данные из контекста, и предположить какие ключевые слова будут вводиться пользователем при поиске товара из тех, которые ты получил.
        

        Список ключевых слов для фильтрации: {{keywords_list}}

        Формат ответа: ТОЛЬКО список из {max_keywords} ключевых слов через запятую, без нумерации, без пояснений.
        """

        return system_prompt, user_prompt

