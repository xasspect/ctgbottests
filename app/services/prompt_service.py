    def get_wb_title_prompt(self, category: str, purposes: List[str],
                            additional_params: List[str], keywords: List[str]) -> tuple[str, str]:
        """
        Оптимизированный промпт для генерации заголовка Wildberries для gpt-4o-mini

        Returns:
            Кортеж (system_prompt, user_prompt)
        """
        purposes_text = ", ".join(purposes) if purposes else ""
        params_text = ", ".join(additional_params) if additional_params else ""
        top_keywords = keywords[:15] if keywords else []
        keywords_text = ", ".join(top_keywords)

        # ОПТИМИЗИРОВАННЫЙ системный промпт (сокращен на 70%)
        system_prompt = """Ты создаешь заголовки для Wildberries по строгим правилам.

    **Обязательные правила:**
    1. Только факты, без оценок и эмоций
    2. Без знаков препинания (только пробелы)
    3. Без капслока (кроме ПВХ, 3D)
    4. Максимум 60 символов
    5. Никогда не используй: качественный, лучший, премиум, супер, топ, хит

    **Структура заголовка:**
    1. Самый частотный запрос из MPStats
    2. [ПВХ] если панели
    3. Частотное назначение
    4. 1-2 ключа из комбинации
    5. Конкретная характеристика

    **Пример правильного заголовка:**
    панели для стен пвх под камень влагостойкие 2700х250 мм"""

        # ОПТИМИЗИРОВАННЫЙ пользовательский промпт (сокращен на 60%)
        user_prompt = f"""**Данные товара:**
    - Категория: {category}
    - Назначения: {purposes_text}
    - Параметры: {params_text}
    - Ключевые слова: {keywords_text}

    **Алгоритм создания:**
    1. Выбери самый частотный запрос из ключевых слов для категории "{category}"
    2. Добавь самое частотное назначение из "{purposes_text}"
    3. Если категория содержит "панели" - добавь "ПВХ" после первой фразы
    4. Выбери 1-2 ключа, которые сочетают категорию + назначение
    5. Добавь характеристику: размеры, количество или материал (только если есть в параметрах)

    **Важные ограничения:**
    - Длина: строго до 60 символов
    - Формат: только строчные буквы (кроме ПВХ, 3D)
    - Без знаков препинания, кавычек, номеров

    **Проверь перед ответом:**
    1. Считай символы: должно быть ≤60
    2. Убери все знаки препинания
    3. Проверь, нет ли запрещенных слов
    4. Убедись, что структура соответствует алгоритму

    **Верни только готовый заголовок, без пояснений.**"""

        return system_prompt, user_prompt

