    def _translate_purposes_to_russian(self, purposes: List[str]) -> List[str]:
        """
        Перевод английских назначений на русский

        Args:
            purposes: Список назначений (могут быть на английском)

        Returns:
            Список назначений на русском
        """
        if not purposes:
            return []

        # Маппинг назначений на русский (такой же как в скрапере)
        purpose_map = {
            "wood": "под дерево",
            "with_pattern": "с рисунком",
            "kitchen": "кухня",
            "tile": "плитка",
            "3d": "3D",
            "in_roll": "в рулоне",
            "self_adhesive": "самоклеящиеся",
            "stone": "под камень",
            "bathroom": "ванная",
            "bedroom": "спальня",
            "brick": "под кирпич",
            "marble": "под мрамор",
            "living_room": "гостиная",
            "white": "белый"
        }

        translated_purposes = []

        for purpose in purposes:
            if isinstance(purpose, str):
                # Ищем точное совпадение
                purpose_lower = purpose.lower()
                if purpose_lower in purpose_map:
                    translated_purposes.append(purpose_map[purpose_lower])
                else:
                    # Проверяем частичное совпадение
                    found = False
                    for eng_key, rus_value in purpose_map.items():
                        if eng_key in purpose_lower:
                            translated_purposes.append(rus_value)
                            found = True
                            break
                    # Если не нашли совпадение, оставляем как есть
                    if not found:
                        translated_purposes.append(purpose)

        return translated_purposes

